from odoo import fields, models


class SlackChannel(models.Model):
    """Channel for holding Slack channel"""
    _name = 'slack.channel'
    _description = 'Slack Channels'

    name = fields.Char(string="Name", help='Name of Channel')
    res_company_id = fields.Many2one("res.company",
                                     help="Company of Channel",
                                     string='Company')
